package wic.lp;

import javax.xml.bind.ValidationException;

import static wic.lp.validations.InputValidation.validateCountOfParameters;
import static wic.lp.validations.InputValidation.validateURL;

public class StartApplication {

    /******************************************************************************************************************/
    //=========================================== KONSTANTNI ATRIBUTY TRIDY ============================================
    //================================================ ATRIBUTY TRIDY ==================================================
    //========================================= KONSTANTNI ATRIBUTY INSTANCE ===========================================
    //============================================== ATRIBUTY INSTANCE =================================================

    /******************************************************************************************************************/
    //============================================ PRISTUPOVE METODY TRIDY =============================================
    //================================================= METODY TRIDY ===================================================
    //================================================== KONSTRUKTOR ===================================================

    /**
     * Start program.
     *
     * @param args
     */
    public StartApplication(String[] args) throws ValidationException {
        validateCountOfParameters(args);
        validateURL(args[0]);
    }

    //================================================ TOVARNI METODA ==================================================

    /******************************************************************************************************************/
    //============================================ KONECNE METODY INSTANCE =============================================
    //========================================== PRISTUPOVE METODY INSTANCE ============================================
    //================================================ METODY INSTANCE =================================================

    /******************************************************************************************************************/
    //================================================ SOUKROME METODY =================================================
    //================================================== MAIN METODA ===================================================

    /**
     * @param Excepted 2 parameters:
     *                 <li>URL</li>
     *                 <li>File destination</li>
     */
    public static void main(String[] args) throws ValidationException {
        new StartApplication(args);
    }

    /******************************************************************************************************************/
    //=============================================== TESTOVACI METODY =================================================
    //================================================ SOUKROME TRIDY ==================================================
}
